#ifndef _RECORDER_H_
#define _RECORDER_H_

/*********************************************************************************************************
*                                              包含头文件
*********************************************************************************************************/
#include "DataType.h"

/*********************************************************************************************************
*                                              宏定义
*********************************************************************************************************/

/*********************************************************************************************************
*                                              枚举结构体
*********************************************************************************************************/
//录音机状态机
typedef enum
{
  RECORDER_IDLE,   //空闲状态
  RECORDER_START,  //开始录音
  RECORDER_REV,    //接收音频数据
  RECORDER_PAUSE,  //暂停录音
  RECORDER_FINISH, //完成录音
}EnumRecorderState;

/*********************************************************************************************************
*                                              API函数声明
*********************************************************************************************************/
void  InitRecorder(void);         //初始化录音机
void  RecorderPoll(void);         //录音轮询任务
void  StartEndRecorder(void);     //开始/继续/结束录音
void  PauseRecorder(void);         //暂停录音
u8    IsRecorderIdle(void);       //判断录音机是否为空闲状态
char* GetRecordName(void);        //获取录音名字
u64   GetRecordTime(void);        //获取录音时长

#endif
