#ifndef _TEXT_WIDGET_H_
#define _TEXT_WIDGET_H_

/*********************************************************************************************************
*                                              包含头文件
*********************************************************************************************************/
#include "GUIConf.h"
#include "GUIPlatform.h"

/*********************************************************************************************************
*                                              宏定义
*********************************************************************************************************/

/*********************************************************************************************************
*                                              枚举结构体
*********************************************************************************************************/
//text控件，该结构体需是静态变量
typedef struct
{
  u32         x0;         //原点横坐标
  u32         y0;         //原点纵坐标
  u32         width;      //宽度，像素点
  u32         height;     //高度，像素点
  EnumGUIFont font;       //字体大小
  u32         color;      //字体颜色
  u32         background; //背景缓冲区首地址，用户提供，需是静态数组，需4字节对齐
  void        (*setText)(void* this, const char* string);  //设置text内容
}StructTextWidget;

/*********************************************************************************************************
*                                              API函数声明
*********************************************************************************************************/
void InitTextWidgetStruct(StructTextWidget* widget); //设置text控件结构体默认值
u8   CreateTextWidget(StructTextWidget* widget);     //创建Text控件

#endif
