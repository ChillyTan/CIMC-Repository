/*********************************************************************************************************
* 函数名称：InitTempHumidityTop
* 函数功能：初始化内部温度与外部温湿度比较实验顶层模块
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
void InitTempHumidityTop(void)
{
  //温度范围0-100℃
  s_structGUIDev.ShowFlag = 0;

  //初始化UI界面设计
  InitGUI(&s_structGUIDev);
}

/*********************************************************************************************************
* 函数名称：TempHumidityTopTask
* 函数功能：内部温度与外部温湿度比较实验顶层模块任务
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
void TempHumidityTopTask(void)
{
  double inTemp, exTemp, exHumidity;

  //获取CPU内部温度测量结果
  inTemp = GetInTemp();
  printf("CPU内部温度值：%.1f℃\r\n", inTemp);

  //获取外部温湿度测量结果
  exTemp = GetSHT20Temp();
  exHumidity = GetSHT20RH();
  printf("外部温湿度：%.1f℃, %0.f%%RH\r\n", exTemp, exHumidity);

  //设置温湿度显示
  s_structGUIDev.setInTemp(inTemp);
  s_structGUIDev.setExtemp(exTemp);
  s_structGUIDev.setHumidity(exHumidity);

  //GUI任务
  GUITask();
}
