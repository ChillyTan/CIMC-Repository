/*********************************************************************************************************
* 函数名称：GetInTemp
* 函数功能：获取CPU内部温度
* 输入参数：void
* 输出参数：void
* 返 回 值：温度值
* 创建日期：2021年07月01日
* 注    意：
*          1、根据参考手册：Temperature (°C) = {(V25 – Vtemperature (digit)) / Avg_Slope} + 25
*          2、根据数据手册：V25=1.45V，Avg_Slope=4.1mV/℃，线性误差：±1.5℃
*********************************************************************************************************/
double GetInTemp(void)
{
  u16 adc;     //ADC值
  double volt; //电压转换结果
  double temp; //CPU内部温度

  //获取CPU内部温度传感器ADC转换结果
  adc = GetInTempADC();

  //计算得到对应的电压值
  volt = 3.3 * adc / 65536.0;
	
  //转换为温度值
  temp = (1.45 - volt) / 0.0041 + 25.0;

  //返回温度值
  return temp;
}
