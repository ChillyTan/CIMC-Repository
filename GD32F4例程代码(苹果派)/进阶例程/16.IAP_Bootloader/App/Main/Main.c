static void InitHardware(void);       //初始化硬件相关的模块
static void InitSoftware(void);       //初始化软件相关的模块

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：InitHardware
* 函数功能：所有的硬件相关的模块初始化函数都放在此函数中
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
static  void  InitHardware(void)
{  
  InitRCU();           //初始化RCC模块
  InitUART0(115200);   //初始化UART模块
  InitFlash();         //初始化Flash读写模块
}

/*********************************************************************************************************
* 函数名称：InitSoftware
* 函数功能：所有的软件相关的模块初始化函数都放在此函数中
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
static  void  InitSoftware(void)
{
  
}

/*********************************************************************************************************
* 函数名称：main
* 函数功能：主函数 
* 输入参数：void
* 输出参数：void
* 返 回 值：int
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
int main(void)
{  
  FATFS fs_my[2];
  FRESULT result;

  InitHardware();   //初始化硬件相关函数
  InitSoftware();   //初始化软件相关函数

  PutString("\r\n");
  PutString("\r\n");
  PutString("-------------------------Bootloader V1.0.0---------------------------\r\n");
  
  //挂载文件系统
  result = f_mount(&fs_my[0], FS_VOLUME_SD, 1);

  //挂载文件系统失败
  if (result != FR_OK)
  {
    PutString(" 挂载文件系统失败\r\n");
  }

  //挂载系统成功，校验App版本，若发现新版本App则更新App程序
  else
  {
    PutString(" 挂载文件系统成功\r\n");
    CheckAppVersion("0:/UPDATE");
  }


  //卸载文件系统
  f_mount(&fs_my[0], FS_VOLUME_SD, 0);

  //跳转至App
  GotoApp(APP_BEGIN_ADDR);

  //跳转失败，进入死循环
  while(1);
}
