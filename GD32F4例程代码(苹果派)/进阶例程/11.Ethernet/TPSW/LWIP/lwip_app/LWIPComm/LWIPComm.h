#ifndef _LWIP_COMM_H_
#define _LWIP_COMM_H_

/*********************************************************************************************************
*                                              包含头文件
*********************************************************************************************************/
#include "DataType.h"

/*********************************************************************************************************
*                                              宏定义
*********************************************************************************************************/
#define LWIP_MAX_DHCP_TRIES 4 //DHCP服务器最大重试次数

/*********************************************************************************************************
*                                              枚举结构体
*********************************************************************************************************/
//lwip控制结构体
typedef struct
{
  u8 mac[6];      //MAC地址
  u8 ip[4];       //本机IP地址
  u8 netmask[4];  //子网掩码
  u8 gateway[4];  //默认网关的IP地址
  u8 dhcpstatus;  //dhcp状态
                  //0,未获取DHCP地址
                  //1,进入DHCP获取状态
                  //2,成功获取DHCP地址
                  //0XFF,获取失败
}StructLWIPDev;

/*********************************************************************************************************
*                                              API函数声明
*********************************************************************************************************/
u8             LWIPCommInit(void);       //LWIP初始化(LWIP启动的时候使用)
void           LWIPReadPackHandle(void); //LWIP接收数据包轮询任务
void           LWIPPeriodicHandle(void); //LWIP轮询任务
StructLWIPDev* GetLWIPCommDev(void);     //获取LWIP设备结构体

#endif
