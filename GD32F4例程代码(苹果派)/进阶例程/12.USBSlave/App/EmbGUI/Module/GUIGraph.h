#ifndef _GUI_GRAPH_H_
#define _GUI_GRAPH_H_

/*********************************************************************************************************
*                                              包含头文件
*********************************************************************************************************/
#include "DataType.h"

/*********************************************************************************************************
*                                              宏定义
*********************************************************************************************************/

/*********************************************************************************************************
*                                              枚举结构体定义
*********************************************************************************************************/
typedef struct
{
  u32 x0;           //起始横坐标
  u32 y0;           //起始纵坐标
  u32 width;        //宽度
  u32 height;       //高度
  u32 xOff;         //横坐标边框大小
  u32 yOff;         //纵坐标边框大小
  u32 backColor;    //背景颜色
  u32 line1Color;   //线条1颜色
  u32 line2Color;   //线条2颜色
  u32 linePointNum; //线条缓冲区中点的数量
  u32 waveSpeed;    //每隔多少个像素点绘制一个点
  u8* line1Buf;     //线条1数据缓冲区，数据范围0-100，数据量要大于或等于显示宽度
  u8* line2Buf;     //线条2数据缓冲区，数据范围0-100，数据量要大于或等于显示宽度
}StructGUIGraphDev;

/*********************************************************************************************************
*                                              API函数声明
*********************************************************************************************************/
void CreateGUIGraph(void);                  //创建动态内存波形显示模块
void GUIGraphAddrPoint(u8 wave1, u8 wave2); //波形增加点
void GUIGraphRefresh(void);                 //刷新显示

#endif
