//GUI设备结构体，需是静态结构体
typedef struct
{
  //最短扫描时间间隔（ms）
  u32 scanTime; 
  
  //回调函数，按键使用电平检测方式，执行回调函数条件有以下两点
  //1、有按键按下，则执行此回调函数，最短间隔scanTime
  //2、键盘状态由"有按键按下"切换至"无按键按下"，此时也会执行一次回调函数，方便用户清除Ctrl、Shift状态
  void (*scanCallback)(StructGUIButtonResult* result); 
}StructGUIDev;

