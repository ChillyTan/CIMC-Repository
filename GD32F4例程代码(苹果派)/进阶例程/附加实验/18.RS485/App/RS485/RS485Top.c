/*********************************************************************************************************
* 函数名称：InitRS485Top
* 函数功能：初始化RS485实验应用层模块
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
void InitRS485Top(void)
{
	//配置RS485的GPIO
  ConfigRS485GPIO();
	
  //串口号显示
  s_structGUIDev.serialPort = "UART5";

  //波特率显示
  s_structGUIDev.baudRate = "115200";
	
  //发送回调函数
  s_structGUIDev.sendCallBack = SendProc;
	
	//初始化UI界面设计
  InitGUI(&s_structGUIDev);
}

