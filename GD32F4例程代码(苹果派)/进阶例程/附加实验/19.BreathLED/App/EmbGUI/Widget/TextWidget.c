static void SetText(void* dev, const char* string); //设置显示内容

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：SetText
* 函数功能：设置显示内容
* 输入参数：dev：控件结构体地址，string：显示内容
* 输出参数：void
* 返 回 值：0-成功创建，其它-创建失败
* 创建日期：2021年07月01日
* 注    意：需要限定字符串长度
*********************************************************************************************************/
static void SetText(void* dev, const char* string)
{
  u32 x0, y0, len;
  StructTextWidget* widget;

  widget = (StructTextWidget*)dev;
  x0 = widget->x0;
  y0 = widget->y0;

  //计算控件最大显示字符数量
  len = widget->width / GetFontWidth(widget->font);

  //绘制背景
  GUIDrawBackground(widget->x0, widget->y0, widget->width, widget->height, widget->background);

  //显示字符串，透明
  GUIDrawTextLine(x0, y0, (u32)string, widget->font, NULL, widget->color, 1, len);
}

/*********************************************************************************************************
*                                              API函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：InitTextWidgetStruct
* 函数功能：设置text控件结构体默认值
* 输入参数：widget：控件首地址
* 输出参数：void
* 返 回 值：0-成功创建，其它-创建失败
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
void InitTextWidgetStruct(StructTextWidget* widget)
{
  widget->x0         = 0;                 //原点横坐标默认值
  widget->y0         = 0;                 //原点纵坐标默认值
  widget->width      = 100;               //控件宽度
  widget->height     = 24;                //控件高度
  widget->font       = GUI_FONT_ASCII_24; //12x24 ASCII码字体
  widget->color      = GUI_COLOR_WHITE;   //字体颜色默认为白色
  widget->background = NULL;              //背景缓冲区首地址
}

/*********************************************************************************************************
* 函数名称：CreateTextWidget
* 函数功能：创建text控件
* 输入参数：widget：控件首地址
* 输出参数：void
* 返 回 值：0-成功创建，其它-创建失败
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
u8 CreateTextWidget(StructTextWidget* widget)
{

  //未给定参数
  if((0 == widget->width) || (0 == widget->height))
  {
    return 1;
  }

  //将背景储存到缓冲区
  GUISaveBackground(widget->x0, widget->y0, widget->width, widget->height, widget->background);

  //设置setText函数指针内容，使得用户可以通过结构体成员setText修改显示内容
  widget->setText = SetText;
  
  return 0;
}
