#include "GD25QXX.h"
#include "SPI.h"

/*********************************************************************************************************
*                                              宏定义
*********************************************************************************************************/
#define W25Q16_PAGE_SIZE    256                            //一页的大小，256字节
#define W25Q16_SECTOR_SIZE (4 * 1024)                      //扇区大小，字节
#define W25Q16_BLOCK_SIZE  (16 * W25Q16_SECTOR_SIZE)
#define W25Q16_SIZE        (32 * W25Q16_BLOCK_SIZE)

/*********************************************************************************************************
*                                              内部变量
*********************************************************************************************************/
const u8 byDUMMY = 0xff;

/*********************************************************************************************************
*                                              枚举结构体定义
*********************************************************************************************************/

/*********************************************************************************************************
*                                              内部函数声明
*********************************************************************************************************/
static void GD25Q16WriteEnable(void);
static void GD25Q16WaitForWriteEnd(void);
static void GD25Q16WriteStatusReg1(u16 status);
static void GD25Q16PageProgram(u8 *buf, u16 len, u32 addr);

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：GD25Q16WriteEnable
* 函数功能：写使能
* 输入参数：void 
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
static void GD25Q16WriteEnable(void)
{
  CLR_SPI_NSS();
  GD25Q16ReadWriteDate(0x06);
  SET_SPI_NSS();
}

/*********************************************************************************************************
* 函数名称：GD25Q16WaitForWriteEnd
* 函数功能：等待FLASH内部时序操作完成
* 输入参数：void 
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
static void GD25Q16WaitForWriteEnd(void)
{
  u8 status = 0;
  CLR_SPI_NSS();
  GD25Q16ReadWriteDate(0x05);
  
  do
  {
    status = GD25Q16ReadWriteDate(byDUMMY);
  }
  while((status & 0x01) == 1);
  
  SET_SPI_NSS();
}

/*********************************************************************************************************
* 函数名称：GD25Q16WriteStatusReg1
* 函数功能：写状态寄存器1 
* 输入参数：status-状态值 
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
static void GD25Q16WriteStatusReg1(u16 status)
{
  GD25Q16WriteEnable();
  CLR_SPI_NSS();
  GD25Q16ReadWriteDate(0x01);
  GD25Q16ReadWriteDate(status & 0xFF);
  GD25Q16ReadWriteDate((status >> 8) & 0xFF);
  SET_SPI_NSS();
  GD25Q16WaitForWriteEnd();
}

/*********************************************************************************************************
* 函数名称：GD25Q16PageProgram
* 函数功能：页写 
* 输入参数：buf-要写的数据,len-数据长度,addr-起始地址
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
static void GD25Q16PageProgram(u8 *buf, u16 len, u32 addr)
{
  GD25Q16WriteEnable();
  CLR_SPI_NSS();
  GD25Q16ReadWriteDate(0x02);
  GD25Q16ReadWriteDate((addr & 0xFF0000) >> 16);
  GD25Q16ReadWriteDate((addr & 0x00FF00) >> 8);
  GD25Q16ReadWriteDate(addr & 0xFF);
  
  while (len--)
  {
    GD25Q16ReadWriteDate(*buf);
    buf++;
  }
  
  SET_SPI_NSS();
  GD25Q16WaitForWriteEnd();
}

