/*********************************************************************************************************
* 函数名称：W25q64_Write
* 函数功能：数据存储 
* 输入参数：buf-要写的数据,len-数据长度,addr-起始地址 
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
void GD25Q16Write(u8 *buf, u16 len, u32 addr)
{
  u8 pagenum;
  u8 addrbyte;  //最低八位地址
  addrbyte = addr % W25Q16_PAGE_SIZE;
  
  if (len > (W25Q16_PAGE_SIZE - addrbyte)) //跨页
  {
    GD25Q16PageProgram(buf, W25Q16_PAGE_SIZE - addrbyte, addr);//写满本页
    addr += W25Q16_PAGE_SIZE-addrbyte;
    buf += W25Q16_PAGE_SIZE-addrbyte;
    len -= W25Q16_PAGE_SIZE-addrbyte;
    pagenum = len/W25Q16_PAGE_SIZE;
    
    while (pagenum--)
    {
      GD25Q16PageProgram(buf, W25Q16_PAGE_SIZE, addr);
      addr += W25Q16_PAGE_SIZE;
      buf += W25Q16_PAGE_SIZE;
      len -= W25Q16_PAGE_SIZE;
    }
    
    GD25Q16PageProgram(buf, len, addr);
  }
  else
  {
    GD25Q16PageProgram(buf, len, addr);
  }
}

/*********************************************************************************************************
* 函数名称：GD25Q16ReadStatus
* 函数功能：读状态寄存器 
* 输入参数：void 
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
u16 GD25Q16ReadStatus(void)
{
  u16 status=0;
  
  CLR_SPI_NSS();
  GD25Q16ReadWriteDate(0x05);
  status = GD25Q16ReadWriteDate(byDUMMY);
  SET_SPI_NSS();
  CLR_SPI_NSS();
  GD25Q16ReadWriteDate(0x35);
  status |= GD25Q16ReadWriteDate(byDUMMY) << 8;
  SET_SPI_NSS();
  
  return status;
}

/*********************************************************************************************************
* 函数名称：GD25Q16ReadDeviceID
* 函数功能：读芯片ID
* 输入参数：void 
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
u16 GD25Q16ReadDeviceID(void)
{
  u8 DeviceID = 0;
  
  CLR_SPI_NSS();
  GD25Q16ReadWriteDate(0xAB);
  GD25Q16ReadWriteDate(byDUMMY);
  GD25Q16ReadWriteDate(byDUMMY);
  GD25Q16ReadWriteDate(byDUMMY);
  DeviceID = GD25Q16ReadWriteDate(0X00);
  SET_SPI_NSS();
  
  return DeviceID;
}

/*********************************************************************************************************
* 函数名称：GD25Q16ReadJEDEID
* 函数功能：读芯片ID 
* 输入参数：void 
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
u16 GD25Q16ReadJEDEID(void)
{
  u16 DeviceID = 0;
  
  CLR_SPI_NSS();
  GD25Q16ReadWriteDate(0x9F);
  GD25Q16ReadWriteDate(byDUMMY);
  DeviceID = GD25Q16ReadWriteDate(byDUMMY) << 8;
  DeviceID |= GD25Q16ReadWriteDate(byDUMMY);
  SET_SPI_NSS();
  
  return DeviceID;
}

/*********************************************************************************************************
* 函数名称：GD25Q16Read
* 函数功能：读数据
* 输入参数：buf-数据缓冲区,len-长度,addr-起始地址 
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
void GD25Q16Read(u8 *buf, u32 len, u32 addr)
{
  u32 i;
  
  CLR_SPI_NSS();
  GD25Q16ReadWriteDate(0x03);
  GD25Q16ReadWriteDate((addr & 0xFF0000) >> 16);
  GD25Q16ReadWriteDate((addr & 0x00FF00) >> 8);
  GD25Q16ReadWriteDate(addr & 0xFF);
  
  for (i = 0; i < len; i++)
  {
    buf[i] = GD25Q16ReadWriteDate(byDUMMY);
  }
  
  SET_SPI_NSS();
}

