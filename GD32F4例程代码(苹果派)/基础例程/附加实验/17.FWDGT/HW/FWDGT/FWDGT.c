static  void ConfigFWDGT(unsigned char prer, unsigned short rlr);       //配置FWDGT

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：ConfigFWDGT
* 函数功能：配置FWDGT 
* 输入参数：prer为分频数，最大值是256，rlr为重装载寄存器值，低11位有效（[11:0]，共12位）
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
static void ConfigFWDGT(unsigned char prer, unsigned short rlr) 
{  
  //配置独立看门狗定时器
  fwdgt_write_enable();          //解除寄存器写保护
  
  fwdgt_config(rlr, prer);       //设置重装载值和预分频值
    
  fwdgt_counter_reload();        //将FWDGT_RLD寄存器的值重装载FWDGT计数器
  
  fwdgt_enable();                //使能独立看门狗定时器
}

/*********************************************************************************************************
*                                              API函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：InitFWDGT
* 函数功能：初始化FWDGT模块 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：自动重装载寄存器的低12位有效（[11:0]），则最大值为0xFFF = 4095
*           预分频寄存器是八位有效寄存器，分频因子有[4,8,16,32,64,128,256,256]
*********************************************************************************************************/
void InitFWDGT(void)
{   
  ConfigFWDGT(FWDGT_PSC_DIV64, 511);    //配置独立看门狗定时器，溢出时间为1s
}

/*********************************************************************************************************
* 函数名称：FeedFWDGT
* 函数功能：喂独立看门狗 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
void FeedFWDGT(void)
{
  fwdgt_counter_reload();      //按照FWDGT重装载寄存器的值重装载FWDGT计数器
}
