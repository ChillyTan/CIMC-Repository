enum Day     //星期
{ Mon = 1, Tues, Wed, Thur, Fri, Sat, Sun };

enum months  //月份
{ JAN = 1, FEB, MAR, APR, MAY, JUN, JUL, AUG, SEP, OCT, NOV, DEC };
