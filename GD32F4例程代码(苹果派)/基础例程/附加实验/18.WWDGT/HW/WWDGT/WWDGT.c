static void ConfigWWDGT(void);       //配置WWWDG

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：ConfigWWDGT
* 函数功能：配置WWDGT
* 输入参数：
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：WWDGT clock = (PCLK1 (60MHz)/4096)/8 = 1831Hz (~546 us)
*           window : ~546 * (127-80)= 25.7ms < refresh window < ~546 * (127-63) =34.9ms
*********************************************************************************************************/
static void ConfigWWDGT(void) 
{
  rcu_periph_clock_enable(RCU_WWDGT);         //使能WWDGT时钟

  wwdgt_config(127, 80, WWDGT_CFG_PSC_DIV8);  //设置计数值127,窗口值80,分频系数8

  wwdgt_enable();                             //使能窗口看门狗定时器
}

/*********************************************************************************************************
*                                              API函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：InitWWDGT
* 函数功能：初始化WWDGT模块 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
void InitWWDGT(void)
{   
  ConfigWWDGT();                //配置WWDGT
}

/*********************************************************************************************************
* 函数名称：FeedWWDGT
* 函数功能：喂狗
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
void FeedWWDGT(void)
{
  wwdgt_counter_update(127);    //喂狗
}
