/*********************************************************************************************************
* 函数名称：Proc2msTask
* 函数功能：2ms处理任务 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2022年01月01日
* 注    意：
*********************************************************************************************************/
static  void  Proc2msTask(void)
{  
  static u8 s_Cnt5 = 0;
  unsigned char recData;
  
  if(Get2msFlag())  //判断2ms标志位状态
  { 
    LEDFlicker(250);//调用闪烁函数
    
    //20ms任务
    if(s_Cnt5 >= 4)
    {
      ScanKeyOne(KEY_NAME_KEY1, ProcKeyUpKey1, ProcKeyDownKey1);
      ScanKeyOne(KEY_NAME_KEY2, ProcKeyUpKey2, ProcKeyDownKey2);
      ScanKeyOne(KEY_NAME_KEY3, ProcKeyUpKey3, ProcKeyDownKey3);

      s_Cnt5 = 0;
    }
    else
    {
      s_Cnt5++;
    }

    while(ReadUART0(&recData, 1))
    {
      recData++;
     
      WriteUART0(&recData, 1);
    }

    Clr2msFlag();   //清除2ms标志位
  }
}

/*********************************************************************************************************
* 函数名称：Proc1SecTask
* 函数功能：1s处理任务 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2022年01月01日
* 注    意：
*********************************************************************************************************/
static  void  Proc1SecTask(void)
{ 
  if(Get1SecFlag()) //判断1s标志位状态
  {
		rtc_show_time();
    
    Clr1SecFlag();  //清除1s标志位
  }    
}

